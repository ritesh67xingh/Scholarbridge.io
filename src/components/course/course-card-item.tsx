interface Props {
  item: Course
}

// âœ… Added this function
function formatNumber(n: number): string {
  if (n >= 1000) {
    return (n / 1000).toFixed(n % 1000 === 0 ? 0 : 1) + 'k'
  }
  return n.toString()
}

